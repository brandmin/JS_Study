// 자바스크립트 연산자: 프로그래밍 언어에서 특정 연산을 수행할 수 있는 연산자
// 1. 산술연산자
let num1 = 10;
let num2 = 5;

console.log(num1 + num2);
console.log(num1 - num2);
console.log(num1 * num2);
console.log(num1 / num2);
console.log(num1 % 2);
console.log(num2 % 2);

// 2. 증감연산자
let num = 10;
console.log(num++); // 후위연산: 10
console.log(num); // 현재 num 값: 11
console.log(++num); // 전위연산: 12
console.log(num2--);
console.log(num2);
console.log(--num2);

